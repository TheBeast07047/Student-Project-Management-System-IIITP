/**
 * GET /allocation/admin/faculty-capacity
 * Get all faculty with their capacity info
 */
exports.getFacultyCapacity = async (req, res) => {
    try {
        const semester = parseInt(req.query.semester) || 5;
        const academicYear = req.query.academicYear || await getCurrentAcademicYear();
        const defaultCapacity = await SystemConfig.getConfigValue('sem5.allocation.defaultFacultyCapacity', 4);

        const faculty = await Faculty.find({ isRetired: false }).select('fullName facultyId department mentorCapacity currentMentorCount');

        // Get current run (lightweight — no deep populates)
        const run = await AllocationRun.findOne({ semester, academicYear })
            .sort({ createdAt: -1 })
            .lean();

        const facultyData = faculty.map(f => {
            const cap = (f.mentorCapacity !== null && f.mentorCapacity !== undefined) ? f.mentorCapacity : defaultCapacity;
            let assigned = 0;
            if (run) {
                assigned = run.results.filter(r => r.faculty.toString() === f._id.toString()).length;
                assigned += run.adminPool.filter(p => p.assignedFaculty && p.assignedFaculty.toString() === f._id.toString()).length;
            }
            return {
                _id: f._id,
                fullName: f.fullName,
                facultyId: f.facultyId,
                department: f.department,
                capacity: cap,
                assigned,
                remaining: cap - assigned
            };
        });

        res.json({ success: true, data: facultyData });
    } catch (error) {
        console.error('Error getting faculty capacity:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

