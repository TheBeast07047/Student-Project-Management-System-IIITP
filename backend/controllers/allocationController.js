/**
 * POST /allocation/student/preferences
 * Submit/update ranked faculty preference list
 */
exports.submitGroupPreferences = async (req, res) => {
    try {
        const semester = 5;
        const academicYear = await getCurrentAcademicYear();
        const { preferences } = req.body; // Array of { faculty: facultyId, priority: number }

        // Validate window is open
        const window = await SystemConfig.getConfigValue('sem5.allocation.groupPreferenceWindow', { start: null, end: null });
        if (!isWindowOpen(window)) {
            return res.status(400).json({ success: false, message: 'Group preference submission window is not open' });
        }

        // Validate preference count
        const minPrefs = await SystemConfig.getConfigValue('sem5.allocation.minGroupPreferences', 3);
        const maxPrefs = await SystemConfig.getConfigValue('sem5.allocation.maxGroupPreferences', 7);

        if (!preferences || preferences.length < minPrefs) {
            return res.status(400).json({
                success: false,
                message: `Please select at least ${minPrefs} faculty preferences`
            });
        }
        if (preferences.length > maxPrefs) {
            return res.status(400).json({
                success: false,
                message: `Maximum ${maxPrefs} faculty preferences allowed`
            });
        }

        // Find student and their group
        const student = await Student.findOne({ user: req.user.id });
        if (!student) return res.status(404).json({ success: false, message: 'Student not found' });

        const group = await Group.findOne({
            semester,
            academicYear,
            'members.student': student._id,
            'members.isActive': true,
            status: 'finalized'
        });

        if (!group) {
            return res.status(400).json({ success: false, message: 'You must be in a finalized group to submit preferences' });
        }

        // Find the group's project
        const Project = require('../models/Project');
        const project = await Project.findOne({
            semester,
            academicYear,
            group: group._id
        });

        if (!project) {
            return res.status(400).json({ success: false, message: 'You must register a project before submitting faculty preferences' });
        }

        // Check student is leader
        if (group.leader.toString() !== student._id.toString()) {
            return res.status(403).json({ success: false, message: 'Only the group leader can submit faculty preferences' });
        }

        // Find or create FacultyPreference record
        let pref = await FacultyPreference.findOne({
            student: student._id,
            semester,
            academicYear
        });

        const formattedPrefs = preferences.map((p, idx) => ({
            faculty: p.faculty,
            priority: idx + 1
        }));

        if (pref) {
            pref.preferences = formattedPrefs;
            pref.status = 'pending';
            await pref.save();
        } else {
            pref = new FacultyPreference({
                student: student._id,
                project: project._id,
                semester,
                academicYear,
                preferences: formattedPrefs,
                status: 'pending'
            });
            await pref.save();
        }

        res.json({
            success: true,
            message: 'Faculty preferences submitted successfully',
            data: { preferences: formattedPrefs }
        });
    } catch (error) {
        console.error('Error submitting group preferences:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

/**
 * GET /allocation/student/demand
 * Get demand indicators for each faculty
 */
exports.getFacultyDemand = async (req, res) => {
    try {
        const semester = 5;
        const academicYear = await getCurrentAcademicYear();

        const showDemand = await SystemConfig.getConfigValue('sem5.allocation.showDemandIndicators', true);
        if (!showDemand) {
            return res.json({ success: true, data: {} });
        }

        // Get all preferences
        const allPrefs = await FacultyPreference.find({
            semester,
            academicYear,
            status: { $ne: 'cancelled' },
            'preferences.0': { $exists: true }
        });

        // Count how many groups listed each faculty
        const demand = {};
        for (const pref of allPrefs) {
            for (const p of pref.preferences) {
                const facId = p.faculty.toString();
                demand[facId] = (demand[facId] || 0) + 1;
            }
        }

        // Count first-choice demand
        const firstChoiceDemand = {};
        for (const pref of allPrefs) {
            const sorted = [...pref.preferences].sort((a, b) => a.priority - b.priority);
            if (sorted.length > 0) {
                const facId = sorted[0].faculty.toString();
                firstChoiceDemand[facId] = (firstChoiceDemand[facId] || 0) + 1;
            }
        }

        res.json({
            success: true,
            data: { totalDemand: demand, firstChoiceDemand }
        });
    } catch (error) {
        console.error('Error getting faculty demand:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

/**
 * GET /allocation/student/status
 * Get allocation result for the student's group (after confirmed)
 */
exports.getStudentAllocationStatus = async (req, res) => {
    try {
        const semester = 5;
        const academicYear = await getCurrentAcademicYear();

        const student = await Student.findOne({ user: req.user.id });
        if (!student) return res.status(404).json({ success: false, message: 'Student not found' });

        const group = await Group.findOne({
            semester,
            academicYear,
            'members.student': student._id,
            'members.isActive': true
        });

        if (!group) {
            return res.json({ success: true, data: { status: 'no_group' } });
        }

        // Find confirmed run
        const run = await AllocationRun.findOne({
            semester, academicYear, status: 'confirmed'
        })
            .populate('results.faculty', 'fullName facultyId department prefix')
            .populate('adminPool.assignedFaculty', 'fullName facultyId department prefix');

        if (!run) {
            return res.json({ success: true, data: { status: 'pending', message: 'Allocation in progress — please wait' } });
        }

        // Check if group is in results
        const result = run.results.find(r => r.group.toString() === group._id.toString());
        if (result) {
            return res.json({
                success: true,
                data: {
                    status: 'allocated',
                    faculty: result.faculty,
                    matchedRound: result.matchedRound,
                    preferenceRank: result.preferenceRank,
                    allocatedBy: result.allocatedBy
                }
            });
        }

        // Check if group is in admin pool (assigned)
        const poolEntry = run.adminPool.find(p => p.group.toString() === group._id.toString());
        if (poolEntry && poolEntry.assignedFaculty) {
            return res.json({
                success: true,
                data: {
                    status: 'allocated',
                    faculty: poolEntry.assignedFaculty,
                    allocatedBy: 'admin'
                }
            });
        }

        if (poolEntry) {
            return res.json({
                success: true,
                data: {
                    status: 'admin_pool',
                    message: 'Your group is being reviewed by the admin for manual assignment'
                }
            });
        }

        return res.json({ success: true, data: { status: 'pending' } });
    } catch (error) {
        console.error('Error getting student allocation status:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

