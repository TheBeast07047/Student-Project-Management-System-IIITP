/**
 * GET /allocation/faculty/groups-to-rank
 * Get groups that listed this faculty in their preferences
 */
exports.getGroupsToRank = async (req, res) => {
    try {
        const semester = 5;
        const academicYear = await getCurrentAcademicYear();

        const faculty = await Faculty.findOne({ user: req.user.id });
        if (!faculty) return res.status(404).json({ success: false, message: 'Faculty not found' });

        // Get faculty ranking window status
        const window = await SystemConfig.getConfigValue('sem5.allocation.facultyRankingWindow', { start: null, end: null });

        // Find all groups that listed this faculty
        const prefs = await FacultyPreference.find({
            semester,
            academicYear,
            status: { $ne: 'cancelled' },
            'preferences.faculty': faculty._id
        }).populate('student', 'fullName studentId');

        // Get the groups for these students
        const groupIds = new Set();
        const groupDetails = [];

        for (const pref of prefs) {
            const group = await Group.findOne({
                semester,
                academicYear,
                'members.student': pref.student._id,
                'members.isActive': true,
                status: 'finalized'
            }).populate('members.student', 'fullName studentId');

            if (group && !groupIds.has(group._id.toString())) {
                groupIds.add(group._id.toString());

                // Find the project for this group
                const Project = require('../models/Project');
                const project = await Project.findOne({
                    semester,
                    academicYear,
                    group: group._id
                }).select('title description');

                groupDetails.push({
                    _id: group._id,
                    name: group.name,
                    members: group.members.filter(m => m.isActive).map(m => ({
                        fullName: m.student.fullName,
                        studentId: m.student.studentId,
                        role: m.role
                    })),
                    project: project ? { title: project.title, description: project.description } : null,
                    createdAt: group.createdAt
                });
            }
        }

        // Get existing ranking if any
        const existing = await FacultyRanking.getByFaculty(faculty._id, semester, academicYear);

        res.json({
            success: true,
            data: {
                windowStatus: getWindowStatus(window),
                groups: groupDetails,
                existingRanking: existing ? existing.rankedGroups.sort((a, b) => a.rank - b.rank) : [],
                hasSubmitted: existing ? !existing.isAutoGenerated && !!existing.submittedAt : false,
                submittedAt: existing ? existing.submittedAt : null
            }
        });
    } catch (error) {
        console.error('Error getting groups to rank:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

/**
 * POST /allocation/faculty/rankings
 * Submit/update ranked group list
 */
exports.submitFacultyRankings = async (req, res) => {
    try {
        const semester = 5;
        const academicYear = await getCurrentAcademicYear();
        const { rankings } = req.body; // Array of { group: groupId, rank: number }

        // Validate window is open
        const window = await SystemConfig.getConfigValue('sem5.allocation.facultyRankingWindow', { start: null, end: null });
        if (!isWindowOpen(window)) {
            return res.status(400).json({ success: false, message: 'Faculty ranking submission window is not open' });
        }

        const faculty = await Faculty.findOne({ user: req.user.id });
        if (!faculty) return res.status(404).json({ success: false, message: 'Faculty not found' });

        // Validate no duplicate ranks
        const ranks = rankings.map(r => r.rank);
        if (new Set(ranks).size !== ranks.length) {
            return res.status(400).json({ success: false, message: 'Rankings must have unique rank values' });
        }

        // Format rankings
        const formattedRankings = rankings.map(r => ({
            group: r.group,
            rank: r.rank
        }));

        // Upsert ranking
        await FacultyRanking.findOneAndUpdate(
            { faculty: faculty._id, semester, academicYear },
            {
                faculty: faculty._id,
                semester,
                academicYear,
                rankedGroups: formattedRankings,
                isAutoGenerated: false,
                submittedAt: new Date()
            },
            { upsert: true, new: true }
        );

        // Update faculty submission flag
        faculty.hasSubmittedRankings = true;
        await faculty.save();

        res.json({
            success: true,
            message: 'Group rankings submitted successfully',
            data: { rankings: formattedRankings }
        });
    } catch (error) {
        console.error('Error submitting faculty rankings:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

/**
 * GET /allocation/faculty/status
 * Get faculty allocation status and window info
 */
exports.getFacultyStatus = async (req, res) => {
    try {
        const semester = 5;
        const academicYear = await getCurrentAcademicYear();

        const faculty = await Faculty.findOne({ user: req.user.id });
        if (!faculty) return res.status(404).json({ success: false, message: 'Faculty not found' });

        const window = await SystemConfig.getConfigValue('sem5.allocation.facultyRankingWindow', { start: null, end: null });
        const ranking = await FacultyRanking.getByFaculty(faculty._id, semester, academicYear);

        // Check if allocation is confirmed
        const run = await AllocationRun.findOne({
            semester, academicYear, status: 'confirmed'
        });

        // Get assigned groups if confirmed
        let assignedGroups = [];
        if (run) {
            const myResults = run.results.filter(r => r.faculty.toString() === faculty._id.toString());
            const groupIds = myResults.map(r => r.group);
            assignedGroups = await Group.find({ _id: { $in: groupIds } })
                .populate('members.student', 'fullName studentId')
                .select('name members');
        }

        res.json({
            success: true,
            data: {
                windowStatus: getWindowStatus(window),
                hasSubmitted: ranking ? !ranking.isAutoGenerated && !!ranking.submittedAt : false,
                submittedAt: ranking ? ranking.submittedAt : null,
                rankedCount: ranking ? ranking.rankedGroups.length : 0,
                allocationConfirmed: !!run,
                assignedGroups
            }
        });
    } catch (error) {
        console.error('Error getting faculty status:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};

/**
 * GET /allocation/faculty-list
 * Get all available faculty for student preference selection
 */
exports.getAvailableFaculty = async (req, res) => {
    try {
        const defaultCapacity = await SystemConfig.getConfigValue('sem5.allocation.defaultFacultyCapacity', 4);

        const faculty = await Faculty.find({
            isRetired: false,
            mode: { $in: ['Regular', 'Adjunct', 'On Lien'] }
        }).select('fullName facultyId department prefix designation mentorCapacity');

        // Filter out faculty with 0 capacity
        const available = faculty.filter(f => {
            const cap = (f.mentorCapacity !== null && f.mentorCapacity !== undefined) ? f.mentorCapacity : defaultCapacity;
            return cap > 0;
        });

        res.json({
            success: true,
            data: available.map(f => ({
                _id: f._id,
                fullName: f.fullName,
                facultyId: f.facultyId,
                department: f.department,
                prefix: f.prefix,
                designation: f.designation
            }))
        });
    } catch (error) {
        console.error('Error getting available faculty:', error);
        res.status(500).json({ success: false, message: error.message });
    }
};
