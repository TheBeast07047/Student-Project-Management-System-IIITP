// ====================================================
// Helper: Get current academic year
// ====================================================
async function getCurrentAcademicYear() {
    return await SystemConfig.getConfigValue('academic.currentYear', '2025-26');
}

// Helper: Check if a date window is currently open
function isWindowOpen(windowConfig) {
    if (!windowConfig || !windowConfig.start || !windowConfig.end) return false;
    const now = new Date();
    return now >= new Date(windowConfig.start) && now <= new Date(windowConfig.end);
}

function getWindowStatus(windowConfig) {
    if (!windowConfig || !windowConfig.start || !windowConfig.end) {
        return { status: 'not_configured', start: null, end: null };
    }
    const now = new Date();
    const start = new Date(windowConfig.start);
    const end = new Date(windowConfig.end);

    if (now < start) return { status: 'upcoming', start, end };
    if (now > end) return { status: 'closed', start, end };
    return { status: 'open', start, end };
}

