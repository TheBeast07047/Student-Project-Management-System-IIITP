// Build Preference Maps
// ============================================================

/**
 * Build the data structures needed by the Gale-Shapley algorithm.
 * 
 * Returns:
 *   groupPrefs: Map<groupId, [facultyId, ...]>  (ordered list)
 *   facultyPrefs: Map<facultyId, Map<groupId, rank>>  (rank lookup)
 *   facultyCapacities: Map<facultyId, number>
 *   groupInfo: Map<groupId, groupDoc>
 *   facultyInfo: Map<facultyId, facultyDoc>
 */
async function buildPreferenceMaps(semester, academicYear) {
    const defaultCapacity = await SystemConfig.getConfigValue('sem5.allocation.defaultFacultyCapacity', 4);

    // 1. Get all valid groups
    const groups = await Group.find({
        semester,
        academicYear,
        status: 'finalized'
    });

    const groupMap = new Map();
    for (const g of groups) {
        groupMap.set(g._id.toString(), g);
    }

    // 2. Build group preference lists
    // Each group's preferences come from FacultyPreference (the student who is group leader)
    const groupPrefs = new Map();

    const allFacPrefs = await FacultyPreference.find({
        semester,
        academicYear,
        status: { $ne: 'cancelled' }
    }).sort({ 'preferences.priority': 1 });

    for (const pref of allFacPrefs) {
        // Find which group this student belongs to
        const studentGroup = groups.find(g =>
            g.members.some(m => m.student.toString() === pref.student.toString() && m.isActive)
        );

        if (!studentGroup) continue;

        const groupId = studentGroup._id.toString();

        // Only process one FacultyPreference per group (first one found)
        if (groupPrefs.has(groupId)) continue;

        // Sort preferences by priority and extract faculty IDs
        const sortedPrefs = [...pref.preferences]
            .sort((a, b) => a.priority - b.priority)
            .map(p => p.faculty.toString());

        groupPrefs.set(groupId, sortedPrefs);
    }

    // 3. Build faculty ranking maps and capacities
    const facultyPrefs = new Map();
    const facultyCapacities = new Map();
    const facultyInfo = new Map();

    const allFaculty = await Faculty.find({
        isRetired: false,
        mode: { $in: ['Regular', 'Adjunct', 'On Lien'] }
    });

    for (const fac of allFaculty) {
        const facId = fac._id.toString();
        facultyInfo.set(facId, fac);

        const cap = fac.mentorCapacity !== null && fac.mentorCapacity !== undefined
            ? fac.mentorCapacity
            : defaultCapacity;
        facultyCapacities.set(facId, cap);
    }

    // Load faculty rankings
    const allRankings = await FacultyRanking.find({ semester, academicYear });

    for (const ranking of allRankings) {
        const facId = ranking.faculty.toString();
        const rankMap = new Map();

        for (const rg of ranking.rankedGroups) {
            rankMap.set(rg.group.toString(), rg.rank);
        }

        facultyPrefs.set(facId, rankMap);
    }

    return { groupPrefs, facultyPrefs, facultyCapacities, groupInfo: groupMap, facultyInfo };
}

// ============================================================
// Core Gale-Shapley Algorithm
// ============================================================

/**
 * Run the Gale-Shapley (Hospital-Resident) algorithm.
 * 
 * @param {Map} groupPrefs - Map<groupId, [facultyId, ...]>
 * @param {Map} facultyPrefs - Map<facultyId, Map<groupId, rank>>
 * @param {Map} facultyCapacities - Map<facultyId, capacity>
 * 
 * @returns {{ matches: Map<groupId, {faculty, round, prefRank}>, adminPool: string[], totalRounds: number }}
 */
function runGaleShapley(groupPrefs, facultyPrefs, facultyCapacities) {
    // State tracking
    const proposalPointer = new Map(); // groupId → next index to propose
    const facultyHolds = new Map();     // facultyId → Set of held groupIds
    const groupMatch = new Map();       // groupId → facultyId (current tentative match)
    const adminPool = [];               // groups that exhausted all preferences
    const matchRound = new Map();       // groupId → round when matched

    // Initialize
    for (const [groupId] of groupPrefs) {
        proposalPointer.set(groupId, 0);
    }
    for (const [facId] of facultyCapacities) {
        facultyHolds.set(facId, new Set());
    }

    let round = 0;
    const MAX_ROUNDS = 1000; // Safety cap (should never be reached)

    while (round < MAX_ROUNDS) {
        round++;
        let proposalsMade = false;

        // Step 2: Every unmatched group with remaining preferences proposes
        const proposals = new Map(); // facultyId → [groupId, ...]

        for (const [groupId, prefList] of groupPrefs) {
            // Skip if already matched or in admin pool
            if (groupMatch.has(groupId)) continue;
            if (adminPool.includes(groupId)) continue;

            const pointer = proposalPointer.get(groupId);

            // No more preferences → admin pool
            if (pointer >= prefList.length) {
                adminPool.push(groupId);
                continue;
            }

            const targetFaculty = prefList[pointer];

            // Check if faculty has capacity 0 → skip immediately
            const cap = facultyCapacities.get(targetFaculty) || 0;
            if (cap === 0) {
                proposalPointer.set(groupId, pointer + 1);
                // Don't count this as a "proposal" that changes state meaningfully
                // but do loop again
                proposalsMade = true;
                continue;
            }

            if (!proposals.has(targetFaculty)) {
                proposals.set(targetFaculty, []);
            }
            proposals.get(targetFaculty).push(groupId);
            proposalsMade = true;
        }

        if (!proposalsMade) break; // Termination condition

        // Step 3: Faculty evaluate proposals
        for (const [facId, newProposals] of proposals) {
            const currentHolds = facultyHolds.get(facId) || new Set();
            const capacity = facultyCapacities.get(facId) || 0;
            const facRanking = facultyPrefs.get(facId) || new Map();

            // Combine current holds + new proposals
            const candidatePool = new Set([...currentHolds, ...newProposals]);

            // Rank all candidates
            const ranked = Array.from(candidatePool).sort((a, b) => {
                const rankA = facRanking.get(a);
                const rankB = facRanking.get(b);

                // Ranked beats unranked
                if (rankA !== undefined && rankB === undefined) return -1;
                if (rankA === undefined && rankB !== undefined) return 1;

                // Both ranked: lower rank number = better
                if (rankA !== undefined && rankB !== undefined) return rankA - rankB;

                // Both unranked: maintain original order (stable)
                return 0;
            });

            // Keep top [capacity], reject rest
            const accepted = new Set(ranked.slice(0, capacity));
            const rejected = ranked.slice(capacity);

            // Update holds
            facultyHolds.set(facId, accepted);

            // Process accepted groups
            for (const gId of accepted) {
                if (!groupMatch.has(gId)) {
                    // Newly matched
                    groupMatch.set(gId, facId);
                    matchRound.set(gId, round);
                }
            }

            // Process rejections
            for (const gId of rejected) {
                if (groupMatch.has(gId) && groupMatch.get(gId) === facId) {
                    // Was held but bumped
                    groupMatch.delete(gId);
                    matchRound.delete(gId);
                }
                // Advance proposal pointer
                const currentPointer = proposalPointer.get(gId);
                proposalPointer.set(gId, currentPointer + 1);
            }
        }

        // Step 5: Check termination
        let allSettled = true;
        for (const [groupId] of groupPrefs) {
            if (!groupMatch.has(groupId) && !adminPool.includes(groupId)) {
                const pointer = proposalPointer.get(groupId);
                if (pointer < groupPrefs.get(groupId).length) {
                    allSettled = false;
                    break;
                } else {
                    // Exhausted preferences
                    if (!adminPool.includes(groupId)) {
                        adminPool.push(groupId);
                    }
                }
            }
        }

        if (allSettled) break;
    }

    // Build final matches
    const matches = new Map();
    for (const [groupId, facId] of groupMatch) {
        const prefList = groupPrefs.get(groupId) || [];
        const prefRank = prefList.indexOf(facId) + 1; // 1-indexed
        matches.set(groupId, {
            faculty: facId,
            round: matchRound.get(groupId) || round,
            prefRank
        });
    }

    return { matches, adminPool, totalRounds: round };
}

