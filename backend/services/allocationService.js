/**
 * Full allocation pipeline:
 *  1. Validate pre-conditions
 *  2. Auto-generate rankings for non-submitting faculty
 *  3. Build preference maps
 *  4. Run Gale-Shapley
 *  5. Save results to AllocationRun
 */
async function executeAllocation(semester, academicYear, adminUserId) {
    // Check for existing active run
    const existingRun = await AllocationRun.getActiveRun(semester, academicYear);
    if (existingRun && existingRun.status === 'running') {
        throw new Error('An allocation is already running for this semester');
    }

    // Create or reuse run document
    let run;
    if (existingRun && existingRun.canRerun()) {
        run = existingRun;
        run.status = 'running';
        run.results = [];
        run.adminPool = [];
        run.errorMessage = null;
    } else {
        run = new AllocationRun({
            semester,
            academicYear,
            status: 'running'
        });
    }

    try {
        // Step 1: Load config
        const config = {
            defaultFacultyCapacity: await SystemConfig.getConfigValue('sem5.allocation.defaultFacultyCapacity', 4),
            minGroupPreferences: await SystemConfig.getConfigValue('sem5.allocation.minGroupPreferences', 3),
            maxGroupPreferences: await SystemConfig.getConfigValue('sem5.allocation.maxGroupPreferences', 7),
            tiebreakMethod: await SystemConfig.getConfigValue('sem5.allocation.tiebreakMethod', 'timestamp'),
            autoRankUnsubmittedFaculty: await SystemConfig.getConfigValue('sem5.allocation.autoRankUnsubmittedFaculty', true)
        };
        run.config = config;

        // Step 2: Validate
        const validation = await validatePreRunConditions(semester, academicYear);
        run.validationReport = validation;

        if (!validation.isReady) {
            run.status = 'failed';
            run.errorMessage = validation.errors.join('; ');
            await run.save();
            return run;
        }

        // Step 3: Auto-generate faculty rankings if enabled
        if (config.autoRankUnsubmittedFaculty) {
            await autoGenerateFacultyRankings(semester, academicYear, config.tiebreakMethod);
        }

        // Step 4: Build preference maps
        const { groupPrefs, facultyPrefs, facultyCapacities, groupInfo, facultyInfo } =
            await buildPreferenceMaps(semester, academicYear);

        // Step 5: Run the algorithm
        const { matches, adminPool, totalRounds } =
            runGaleShapley(groupPrefs, facultyPrefs, facultyCapacities);

        // Step 6: Populate results
        run.runAt = new Date();
        run.totalGroups = groupPrefs.size;
        run.matchedGroups = matches.size;
        run.adminPoolGroups = adminPool.length;
        run.totalRounds = totalRounds;
        run.totalCapacity = Array.from(facultyCapacities.values()).reduce((sum, c) => sum + c, 0);

        // Save matched results
        run.results = [];
        for (const [groupId, match] of matches) {
            run.results.push({
                group: new mongoose.Types.ObjectId(groupId),
                faculty: new mongoose.Types.ObjectId(match.faculty),
                matchedRound: match.round,
                preferenceRank: match.prefRank,
                allocatedBy: 'algorithm'
            });
        }

        // Save admin pool
        run.adminPool = adminPool.map(groupId => ({
            group: new mongoose.Types.ObjectId(groupId),
            reason: 'preferences_exhausted'
        }));

        // Add excluded groups to admin pool
        if (run.validationReport && run.validationReport.excludedGroups) {
            for (const excl of run.validationReport.excludedGroups) {
                run.adminPool.push({
                    group: excl.group,
                    reason: 'excluded'
                });
            }
        }

        run.status = 'preview';
        await run.save();

        return run;

    } catch (error) {
        run.status = 'failed';
        run.errorMessage = error.message;
        await run.save();
        throw error;
    }
}

/**
 * Confirm allocation results — makes them visible to all parties.
 * 
 * Note: We avoid MongoDB transactions here so the code works on
 * standalone (non-replica-set) development instances.  The operations
 * are individually idempotent and can be retried safely.
 */
async function confirmAllocation(runId, adminUserId) {
    const run = await AllocationRun.findById(runId);
    if (!run) throw new Error('Allocation run not found');
    if (!run.canConfirm()) throw new Error('Allocation run cannot be confirmed in its current state');

    try {
        // 1. Update run status
        run.status = 'confirmed';
        run.confirmedAt = new Date();
        run.confirmedBy = adminUserId;
        await run.save();

        // 2. Update FacultyPreference and Project records for each matched group
        for (const result of run.results) {
            // Update FacultyPreference for the specific group
            await FacultyPreference.updateOne(
                {
                    group: result.group,
                    semester: run.semester,
                    academicYear: run.academicYear,
                    status: { $ne: 'cancelled' }
                },
                {
                    $set: {
                        status: 'allocated',
                        allocatedFaculty: result.faculty,
                        allocatedBy: result.allocatedBy === 'admin' ? 'admin_allocation' : 'faculty_choice',
                        allocatedAt: new Date()
                    }
                }
            );

            // Update the Project's allocated faculty
            const Project = require('../models/Project');
            await Project.updateOne(
                { group: result.group, semester: run.semester, academicYear: run.academicYear },
                {
                    $set: {
                        faculty: result.faculty,
                        status: 'faculty_allocated',
                        allocatedBy: result.allocatedBy === 'admin' ? 'admin_allocation' : 'faculty_choice'
                    }
                }
            );

            // Update the Group's allocated faculty
            await Group.updateOne(
                { _id: result.group },
                {
                    $set: {
                        allocatedFaculty: result.faculty,
                        allocationStatus: 'allocated'
                    }
                }
            );

            // Increment faculty mentor count
            await Faculty.findByIdAndUpdate(
                result.faculty,
                { $inc: { currentMentorCount: 1 } }
            );
        }

        return run;

    } catch (error) {
        // If something fails mid-way, mark run as failed so admin can retry
        run.status = 'failed';
        run.errorMessage = `Confirmation failed: ${error.message}`;
        await run.save();
        throw error;
    }
}

/**
 * Reset allocation to allow re-run.
 */
async function resetAllocation(runId) {
    const run = await AllocationRun.findById(runId);
    if (!run) throw new Error('Allocation run not found');
    if (run.status === 'confirmed') throw new Error('Cannot reset a confirmed allocation');

    run.status = 'not_run';
    run.results = [];
    run.adminPool = [];
    run.runAt = null;
    run.totalRounds = 0;
    run.matchedGroups = 0;
    run.adminPoolGroups = 0;
    run.errorMessage = null;
    await run.save();

    return run;
}

/**
 * Manually assign faculty to an admin pool group.
 */
async function assignAdminPoolGroup(runId, groupId, facultyId, adminUserId) {
    const run = await AllocationRun.findById(runId);
    if (!run) throw new Error('Allocation run not found');

    const poolEntry = run.adminPool.find(p => p.group.toString() === groupId);
    if (!poolEntry) throw new Error('Group not found in admin pool');

    // Check faculty has remaining capacity
    const defaultCapacity = await SystemConfig.getConfigValue('sem5.allocation.defaultFacultyCapacity', 4);
    const faculty = await Faculty.findById(facultyId);
    if (!faculty) throw new Error('Faculty not found');

    const cap = faculty.mentorCapacity !== null && faculty.mentorCapacity !== undefined
        ? faculty.mentorCapacity
        : defaultCapacity;

    // Count current assignments for this faculty in this run
    const currentAssignments = run.results.filter(r => r.faculty.toString() === facultyId).length;
    const poolAssignments = run.adminPool.filter(
        p => p.assignedFaculty && p.assignedFaculty.toString() === facultyId
    ).length;

    if (currentAssignments + poolAssignments >= cap) {
        throw new Error('Faculty has reached their maximum capacity');
    }

    // Update pool entry
    poolEntry.assignedFaculty = facultyId;
    poolEntry.assignedAt = new Date();
    poolEntry.assignedBy = adminUserId;

    // Also add to results array for consistency
    run.results.push({
        group: new mongoose.Types.ObjectId(groupId),
        faculty: new mongoose.Types.ObjectId(facultyId),
        matchedRound: 0,
        preferenceRank: 0,
        allocatedBy: 'admin'
    });

    run.matchedGroups = run.results.length;
    run.adminPoolGroups = run.adminPool.filter(p => !p.assignedFaculty).length;

    await run.save();
    return run;
}

