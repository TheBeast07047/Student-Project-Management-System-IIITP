/**
 * Gale-Shapley (Hospital-Resident) Algorithm Service
 * 
 * Implements the stable matching algorithm for faculty allocation.
 * Groups are the "proposing" side (residents), faculty are "receiving" (hospitals).
 * 
 * Properties guaranteed:
 *  - Termination: always stops in finite rounds
 *  - Stability: no blocking pair exists
 *  - Group-optimal: best possible stable match for each group
 *  - Completeness: every matchable group gets matched
 */

const mongoose = require('mongoose');
const Group = require('../models/Group');
const Faculty = require('../models/Faculty');
const FacultyPreference = require('../models/FacultyPreference');
const FacultyRanking = require('../models/FacultyRanking');
const AllocationRun = require('../models/AllocationRun');
const SystemConfig = require('../models/SystemConfig');

// ============================================================
// Pre-Algorithm Validation
// ============================================================

/**
 * Validate all pre-conditions before running the algorithm.
 * Returns a validation report with details about eligible groups,
 * faculty participation, and capacity analysis.
 */
async function validatePreRunConditions(semester, academicYear) {
    const report = {
        totalGroups: 0,
        validGroups: 0,
        excludedGroups: [],
        totalFaculty: 0,
        facultyWithRankings: 0,
        facultyWithoutRankings: 0,
        totalCapacity: 0,
        capacityWarning: null,
        isReady: true,
        errors: []
    };

    // 1. Get all finalized groups with preferences for this semester
    const groups = await Group.find({
        semester,
        academicYear,
        status: 'finalized'
    }).populate('members.student', 'fullName studentId');

    report.totalGroups = groups.length;

    // 2. Validate each group
    const validGroupIds = [];
    for (const group of groups) {
        const activeMembers = group.members.filter(m => m.isActive);

        // Check group has enough members
        if (activeMembers.length < (group.minMembers || 2)) {
            report.excludedGroups.push({
                group: group._id,
                name: group.name,
                reason: `Insufficient members (${activeMembers.length}/${group.minMembers || 2})`
            });
            continue;
        }

        // Check group has submitted faculty preferences
        const prefs = await FacultyPreference.findOne({
            student: activeMembers[0].student,
            semester,
            academicYear,
            status: { $ne: 'cancelled' }
        });

        if (!prefs || !prefs.preferences || prefs.preferences.length === 0) {
            report.excludedGroups.push({
                group: group._id,
                name: group.name,
                reason: 'No faculty preferences submitted'
            });
            continue;
        }

        validGroupIds.push(group._id);
    }

    report.validGroups = validGroupIds.length;

    // 3. Get faculty who are listed in at least one group's preferences
    const defaultCapacity = await SystemConfig.getConfigValue('sem5.allocation.defaultFacultyCapacity', 4);

    const allFaculty = await Faculty.find({
        isRetired: false,
        mode: { $in: ['Regular', 'Adjunct', 'On Lien'] }
    });

    report.totalFaculty = allFaculty.length;

    // Count faculty rankings
    const rankings = await FacultyRanking.find({ semester, academicYear });
    const rankedFacultyIds = new Set(rankings.map(r => r.faculty.toString()));

    report.facultyWithRankings = rankings.filter(r => !r.isAutoGenerated).length;
    report.facultyWithoutRankings = allFaculty.length - report.facultyWithRankings;

    // 4. Calculate total capacity
    let totalCapacity = 0;
    for (const fac of allFaculty) {
        const cap = fac.mentorCapacity !== null && fac.mentorCapacity !== undefined
            ? fac.mentorCapacity
            : defaultCapacity;
        totalCapacity += cap;
    }
    report.totalCapacity = totalCapacity;

    // 5. Capacity warning
    if (report.validGroups > totalCapacity) {
        const deficit = report.validGroups - totalCapacity;
        report.capacityWarning = `Capacity shortage: ${report.validGroups} groups but only ${totalCapacity} slots. At least ${deficit} group(s) will require manual allocation.`;
    }

    // 6. Check errors
    if (report.validGroups === 0) {
        report.isReady = false;
        report.errors.push('No valid groups found for allocation');
    }

    return report;
}

// ============================================================
// Tiebreak Helpers
// ============================================================

/**
 * Sort groups by tiebreak method.
 * Used for unranked groups in a faculty's preference list.
 */
function applyTiebreak(groups, method) {
    switch (method) {
        case 'random':
            // Fisher-Yates shuffle
            const shuffled = [...groups];
            for (let i = shuffled.length - 1; i > 0; i--) {
                const j = Math.floor(Math.random() * (i + 1));
                [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
            }
            return shuffled;

        case 'alphabetical':
            return [...groups].sort((a, b) => {
                const nameA = a.name || '';
                const nameB = b.name || '';
                return nameA.localeCompare(nameB);
            });

        case 'timestamp':
        default:
            // Sort by creation timestamp (earliest first = higher priority)
            return [...groups].sort((a, b) => {
                const dateA = a.createdAt || new Date(0);
                const dateB = b.createdAt || new Date(0);
                return dateA - dateB;
            });
    }
}

