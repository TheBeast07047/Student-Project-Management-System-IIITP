/**
 * For faculty who did not submit rankings, auto-generate rankings
 * using the configured tiebreak method.
 */
async function autoGenerateFacultyRankings(semester, academicYear, tiebreakMethod) {
    const autoGenerated = [];

    // Get all faculty listed in any group's preferences
    const allPrefs = await FacultyPreference.find({
        semester,
        academicYear,
        status: { $ne: 'cancelled' }
    });

    // Build a map: facultyId → [groupIds that listed them]
    const facultyToGroups = new Map();
    for (const pref of allPrefs) {
        for (const p of pref.preferences) {
            const facId = p.faculty.toString();
            if (!facultyToGroups.has(facId)) {
                facultyToGroups.set(facId, []);
            }
            // We store the group info; we'll de-duplicate later
            // For now, store student's group reference
        }
    }

    // Get all groups with preferences - build group → faculty prefs map
    const groups = await Group.find({
        semester,
        academicYear,
        status: 'finalized'
    });

    const groupMap = new Map();
    for (const g of groups) {
        groupMap.set(g._id.toString(), g);
    }

    // Re-build: which faculty are listed by which groups?
    // We need to find group IDs from FacultyPreference records
    const facultyGroupMap = new Map();

    for (const pref of allPrefs) {
        // Find which group this student belongs to
        const studentGroup = groups.find(g =>
            g.members.some(m => m.student.toString() === pref.student.toString() && m.isActive)
        );

        if (!studentGroup) continue;

        for (const p of pref.preferences) {
            const facId = p.faculty.toString();
            if (!facultyGroupMap.has(facId)) {
                facultyGroupMap.set(facId, new Set());
            }
            facultyGroupMap.get(facId).add(studentGroup._id.toString());
        }
    }

    // For each faculty that has groups but no ranking, auto-generate
    for (const [facId, groupIdSet] of facultyGroupMap) {
        const existing = await FacultyRanking.findOne({
            faculty: facId,
            semester,
            academicYear,
            isAutoGenerated: false,
            submittedAt: { $ne: null }
        });

        if (existing) continue; // Faculty submitted manually, skip

        // Get the group documents for tiebreaking
        const groupIds = Array.from(groupIdSet);
        const groupDocs = groupIds.map(id => groupMap.get(id)).filter(Boolean);

        // Apply tiebreak to get ranked order
        const sorted = applyTiebreak(groupDocs, tiebreakMethod);

        const rankedGroups = sorted.map((g, idx) => ({
            group: g._id,
            rank: idx + 1
        }));

        // Upsert the auto-generated ranking
        await FacultyRanking.findOneAndUpdate(
            { faculty: facId, semester, academicYear },
            {
                faculty: facId,
                semester,
                academicYear,
                rankedGroups,
                isAutoGenerated: true,
                submittedAt: new Date()
            },
            { upsert: true, new: true }
        );

        autoGenerated.push(facId);
    }

    return autoGenerated;
}

